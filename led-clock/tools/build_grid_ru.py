#!/usr/bin/env python3
"""Build and validate Russian word clock grid (13×15)."""

COLS = 15
GRID = [
    "ОДИНЖДВАЮТРИБШП",  # 0:  ОДИН ДВА ТРИ
    "ЧЕТЫРЕЛПЯТЬГУКМ",  # 1:  ЧЕТЫРЕ ПЯТЬ
    "ШЕСТЬЖСЕМЬГДЦУХ",  # 2:  ШЕСТЬ СЕМЬ
    "ВОСЕМЬХДЕВЯТЬЙШ",  # 3:  ВОСЕМЬ ДЕВЯТЬ
    "ДЕСЯТЬЛКМБЦУЖФХ",  # 4:  ДЕСЯТЬ
    "ОДИННАДЦАТЬГУКМ",  # 5:  ОДИННАДЦАТЬ
    "ДВЕНАДЦАТЬДНЖНЧ",  # 6:  ДВЕНАДЦАТЬ ДН НЧ
    "НОЛЬЖОДИНРДВАБЧ",  # 7:  НОЛЬ ОДИН ДВА (tens)
    "ТРИГЧЕТЫРЕКПЯТЬ",  # 8:  ТРИ ЧЕТЫРЕ ПЯТЬ (tens)
    "НОЛЬЮОДИНМДВАВУ",  # 9:  НОЛЬ ОДИН ДВА (ones)
    "ТРИЗЧЕТЫРЕХПЯТЬ",  # 10: ТРИ ЧЕТЫРЕ ПЯТЬ (ones)
    "ШЕСТЬСЕМЬВОСЕМЬ",  # 11: ШЕСТЬ СЕМЬ ВОСЕМЬ (ones)
    "ДЕВЯТЬЧАСЫЖКЛМН",  # 12: ДЕВЯТЬ ЧАСЫ (ones)
]
ROWS = len(GRID)

HOURS = {
    1:  (0, 0, 4),    # ОДИН
    2:  (0, 5, 8),    # ДВА
    3:  (0, 9, 12),   # ТРИ
    4:  (1, 0, 6),    # ЧЕТЫРЕ
    5:  (1, 7, 11),   # ПЯТЬ
    6:  (2, 0, 5),    # ШЕСТЬ
    7:  (2, 6, 10),   # СЕМЬ
    8:  (3, 0, 6),    # ВОСЕМЬ
    9:  (3, 7, 13),   # ДЕВЯТЬ
    10: (4, 0, 6),    # ДЕСЯТЬ
    11: (5, 0, 11),   # ОДИННАДЦАТЬ
    12: (6, 0, 10),   # ДВЕНАДЦАТЬ
}

AMPM = {
    "AM": (6, 10, 12),  # ДН
    "PM": (6, 13, 15),  # НЧ
}

TENS = {
    0: (7, 0, 4),     # НОЛЬ
    1: (7, 5, 9),     # ОДИН
    2: (7, 10, 13),   # ДВА
    3: (8, 0, 3),     # ТРИ
    4: (8, 4, 10),    # ЧЕТЫРЕ
    5: (8, 11, 15),   # ПЯТЬ
}

ONES = {
    0: (9, 0, 4),     # НОЛЬ
    1: (9, 5, 9),     # ОДИН
    2: (9, 10, 13),   # ДВА
    3: (10, 0, 3),    # ТРИ
    4: (10, 4, 10),   # ЧЕТЫРЕ
    5: (10, 11, 15),  # ПЯТЬ
    6: (11, 0, 5),    # ШЕСТЬ
    7: (11, 5, 9),    # СЕМЬ
    8: (11, 9, 15),   # ВОСЕМЬ
    9: (12, 0, 6),    # ДЕВЯТЬ
}

EXPECTED = {
    "HOURS": {
        1: "ОДИН", 2: "ДВА", 3: "ТРИ", 4: "ЧЕТЫРЕ", 5: "ПЯТЬ",
        6: "ШЕСТЬ", 7: "СЕМЬ", 8: "ВОСЕМЬ", 9: "ДЕВЯТЬ", 10: "ДЕСЯТЬ",
        11: "ОДИННАДЦАТЬ", 12: "ДВЕНАДЦАТЬ",
    },
    "AMPM": {"AM": "ДН", "PM": "НЧ"},
    "TENS": {0: "НОЛЬ", 1: "ОДИН", 2: "ДВА", 3: "ТРИ", 4: "ЧЕТЫРЕ", 5: "ПЯТЬ"},
    "ONES": {
        0: "НОЛЬ", 1: "ОДИН", 2: "ДВА", 3: "ТРИ", 4: "ЧЕТЫРЕ", 5: "ПЯТЬ",
        6: "ШЕСТЬ", 7: "СЕМЬ", 8: "ВОСЕМЬ", 9: "ДЕВЯТЬ",
    },
}


def verify():
    errors = 0
    for i, row in enumerate(GRID):
        n = len(row)
        if n != COLS:
            print(f"  ERROR: row {i} has {n} chars, expected {COLS}: \"{row}\"")
            errors += 1

    for name, mapping in [("HOURS", HOURS), ("AMPM", AMPM), ("TENS", TENS), ("ONES", ONES)]:
        for key, (row, cs, ce) in mapping.items():
            word = GRID[row][cs:ce]
            expected = EXPECTED[name][key]
            ok = "✓" if word == expected else f"✗ expected \"{expected}\""
            print(f"  {name}[{key!r:>4}] = row {row:2d} [{cs:2d}:{ce:2d}] → \"{word}\" {ok}")
            if word != expected:
                errors += 1

    if errors:
        print(f"\n  {errors} ERRORS!")
    else:
        print(f"\n  All OK. Grid: {ROWS}×{COLS} = {ROWS * COLS} LEDs")
    return errors == 0


if __name__ == "__main__":
    verify()
